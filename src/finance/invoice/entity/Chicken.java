package finance.invoice.entity;

public class Chicken {
	private int id;
	private int burma;
	private int cmee;
	private int cp;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getBurma() {
		return burma;
	}

	public void setBurma(int burma) {
		this.burma = burma;
	}

	public int getCmee() {
		return cmee;
	}

	public void setCmee(int cmee) {
		this.cmee = cmee;
	}

	public int getCp() {
		return cp;
	}

	public void setCp(int cp) {
		this.cp = cp;
	}

}
