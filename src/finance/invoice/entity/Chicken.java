package finance.invoice.entity;

public class Chicken {
	private int id;
	private double burma;
	private double cmee;
	private double cp;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getBurma() {
		return burma;
	}

	public void setBurma(double burma) {
		this.burma = burma;
	}

	public double getCmee() {
		return cmee;
	}

	public void setCmee(double cmee) {
		this.cmee = cmee;
	}

	public double getCp() {
		return cp;
	}

	public void setCp(double cp) {
		this.cp = cp;
	}

}
